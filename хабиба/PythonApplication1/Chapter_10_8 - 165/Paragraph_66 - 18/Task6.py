'''
	Функция замены подстроки в строке, где:
	- s - текст в котором нужно заменить подстроку
	- wOld - подстрока которую нужно заменить
	- wNew - подстрока на которую заменяют старую подстроку
'''
def replaceBad(s, wOld, wNew):
	lenOld = len(wOld) # получение размера старой подстроки, для будущего среза в тексте
	res = "" # строка, которая должна быть результатом | не используется
	p = s.find(wOld) # нахождения индекса начала подстроки для будущей замены
	
	# цикл, в которой заменяется подстрока на другую
	while p >= 0:
		s = s[:p] + wNew + s[p+lenOld:] # изменение передаваемой строки
		p = s.find(wOld) # получение индекса старой подстроки после изменения

	return s # возвращение результата


